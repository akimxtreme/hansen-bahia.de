                                        <h3 class="title">Hommage Hansen-Bahia</h3>
                                        <p>Karl-Heinz Hansen, oder Hansen-Bahia. Der 1915 in Hamburg geborene  Künstler und einer der ungewöhnlichsten expressionistischen Holzschneider und Maler der zweiten Hälfte des 20. Jahrhunderts, wandte sich seit 1947 autodidaktisch dem Holzschnitt zu und schuf  Holzschnittfolgen und Einzelblätter religiösen und zeitkritischen Inhalts.</p>
                                        <p>1949 wanderte er nach Brasilien aus. Zunächst war er in São Paulo künstlerischer Berater eines Verlages und hatte auch mehrere große Ausstellungen. Mitte der 50er Jahre übersiedelte er nach Salvador- Bahia, das zu seiner zweiten Heimat wurde. Der berühmte Brasilianische Romancier Jorge Amado fügte dem Namen Hansen als Ehrung »Bahia« hinzu.</p>                                        
                                        <p>Hier, wo er unter Fischern, Matrosen, Händlern und Bettlern lebte und dadurch wesentliche künstlerische Impulse empfing, druckte er auf einer kleinen Handpresse seine Holzschnitte. - In unermüdlicher Arbeit am Holzstock entstand seine unverwechselbare Technik, die ihn zu einem der bedeutendsten deutschen Holzschneider der Nachkriegszeit werden ließ.</p>
                                        <p>1959 kam er für drei Jahre nach Deutschland zurück, bevor er 1963 Professor an der Kunstakademie Addis Abeba/Äthiopien wurde.</p>                                    
