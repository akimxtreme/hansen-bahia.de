<p class="text-justify">Mit fünfundfünfzig Jahren sieht man kritisch in den Spiegel, man sagt sich, Heckel ist am letzten Dienstag unweit seines Alterssitzes verschieden... (Nachrichtenmagazin Der Spiegel vom 2. Februar 1970)</p>

<p class="text-justify">Spiegelgespräche: " Du bist nun fünfundfünfzig Jahre alt, schneidest seit fünfundzwanzig Jahren Holz, Heckel ist tot, du wächst in jene Generation von Holzschneidern hinein, die man die ,Deutsche' nennt, und stehst am Anfang der Kunst."</p>

<p class="text-justify">"Jetzt..." , schreibt Der Spiegel - mal sehen - "Aus provinziellem Muff und Mief drängen deutsche Künstler auf die Höhe der internationalen Avantgarde vor, mit Tierfell-Attrappen, wabernden Schaumstoffpyramiden und sehenswerten Küchenherden, aus denen Schokolade quillt.</p>
