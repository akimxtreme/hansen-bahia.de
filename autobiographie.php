<p class="text-justify">Mit Fünfundfünfzig bilden sich Warzen auf der Oberlippe, das Haar beginnt zu grauen, als Künstler ist man jung wie am ersten Tag.</p>

<p class="text-justify">Das Leben ist kurz. Es reicht nicht aus, um das zu schneiden, was man will. Der Spiegel sagt mir: "Habe Respekt vor den Meistern" - ich weiß, was ich nicht kann.</p>

